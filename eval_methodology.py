# ─── Data Types ───────────────────────────────────────────────────────────────


@dataclass
class BenchmarkQuery:
    """A single evaluation query with ground truth."""
    id: str
    question: str
    category: str  # factual_lookup, cross_reference, cross_jurisdiction, temporal, compliance, unanswerable
    jurisdiction_scope: list  # ["los_angeles", "san_diego"]
    year_scope: list  # ["2019", "2022"]
    expected_answer_short: str
    ground_truth_facts: list  # Key facts that must appear in answer
    required_citations: list  # Code sections that should be cited
    answerable: bool = True
    difficulty: str = "medium"  # easy, medium, hard
    multi_hop_depth: int = 1  # 1=direct, 2+=multi-hop


@dataclass
class EvalResult:
    """Result of evaluating a single query."""
    query_id: str
    mode: str  # "baseline" or "hybrid"
    question: str
    answer: str
    retrieved_chunks: list
    latency_ms: float
    # Metrics (filled by scoring)
    fact_accuracy: float = 0.0
    completeness: float = 0.0
    faithfulness: float = 0.0
    citation_precision: float = 0.0
    citation_recall: float = 0.0
    hallucination_score: float = 0.0
    relevance_score: float = 0.0


