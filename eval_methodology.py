# ─── Baseline Retriever ──────────────────────────────────────────────────────


def baseline_retrieve(query: str, top_k: int = 5) -> dict:
    """Baseline: vector-only retrieval from Supabase."""
    embeddings = OpenAIEmbeddings(model="text-embedding-3-large", api_key=OPENAI_KEY)

    t0 = time.perf_counter()
    query_embedding = embeddings.embed_query(query)
    embed_time = time.perf_counter() - t0

    t1 = time.perf_counter()
    res = httpx.post(
        f"{SUPABASE_URL}/rest/v1/rpc/match_documents",
        headers=HEADERS,
        json={"query_embedding": query_embedding, "match_count": top_k},
        timeout=30,
    )
    retrieval_time = time.perf_counter() - t1

    chunks = res.json() if res.status_code == 200 else []

    return {
        "chunks": chunks,
        "embed_time_ms": embed_time * 1000,
        "retrieval_time_ms": retrieval_time * 1000,
    }


def baseline_answer(query: str, top_k: int = 5) -> EvalResult:
    """Full baseline pipeline: retrieve + generate."""
    t0 = time.perf_counter()

    retrieval = baseline_retrieve(query, top_k)
    chunks = retrieval["chunks"]

    context = "\n\n".join(
        f"Source: {c.get('metadata', {}).get('original_filename', 'unknown')} "
        f"(City: {c.get('metadata', {}).get('city', 'unknown')}, "
        f"Chapter: {c.get('metadata', {}).get('chapter', 'unknown')})\n"
        f"Content: {c.get('content', '')}"
        for c in chunks
    )

    llm = ChatOpenAI(model="gpt-4o", temperature=0, api_key=OPENAI_KEY)
    response = llm.invoke(
        f"Based on the following building code documents, answer the question. "
        f"Cite specific code sections and source documents. "
        f"If the information is not available in the provided context, say so clearly.\n\n"
        f"Context:\n{context}\n\n"
        f"Question: {query}"
    )

    total_time = (time.perf_counter() - t0) * 1000

    return EvalResult(
        query_id="",
        mode="baseline",
        question=query,
        answer=response.content,
        retrieved_chunks=[
            {"content": c.get("content", "")[:200], "metadata": c.get("metadata", {}), "similarity": c.get("similarity", 0)}
            for c in chunks
        ],
        latency_ms=total_time,
    )


def graph_retrieve(query: str) -> list:
    """Retrieve structured context from Neo4j knowledge graph."""
    from neo4j import GraphDatabase

    from config import NEO4J_URI, NEO4J_USER, NEO4J_PASSWORD
    driver = GraphDatabase.driver(
        NEO4J_URI,
        auth=(NEO4J_USER, NEO4J_PASSWORD),
    )

    graph_context = []
    with driver.session() as session:
        # 1. Find cities mentioned in the query
        city_result = session.run(
            "MATCH (c:City) "
            "WHERE toLower($query) CONTAINS toLower(c.display_name) "
            "   OR toLower($query) CONTAINS toLower(c.name) "
            "RETURN c.name AS name, c.display_name AS display, "
            "       c.population AS pop, c.model_code_family AS model_code",
            query=query,
        )
        mentioned_cities = [dict(r) for r in city_result]

        # 2. Get adoption timeline for mentioned cities (or all if none mentioned)
        if mentioned_cities:
            city_names = [c["name"] for c in mentioned_cities]
            timeline = session.run(
                "MATCH (c:City)-[r:ADOPTED]->(ad:AmendmentDoc)-[:BASED_ON]->(mc:ModelCode) "
                "WHERE c.name IN $cities "
                "RETURN c.display_name AS city, ad.year AS year, ad.type AS type, "
                "       mc.edition AS edition, ad.amendment_count AS amendments "
                "ORDER BY c.name, ad.year",
                cities=city_names,
            )
        else:
            timeline = session.run(
                "MATCH (c:City)-[r:ADOPTED]->(ad:AmendmentDoc)-[:BASED_ON]->(mc:ModelCode) "
                "RETURN c.display_name AS city, ad.year AS year, ad.type AS type, "
                "       mc.edition AS edition, ad.amendment_count AS amendments "
                "ORDER BY c.name, ad.year"
            )

        timeline_data = [dict(r) for r in timeline]
        if timeline_data:
            timeline_text = "ADOPTION TIMELINE:\n"
            for t in timeline_data:
                timeline_text += f"  {t['city']} ({t['year']}): {t['edition']} [{t['type']}] - {t['amendments']} amendment markers\n"
            graph_context.append(timeline_text)

        # 3. Find sections amended by multiple cities (cross-jurisdiction)
        shared = session.run(
            "MATCH (c1:City)-[:ADOPTED]->()-[:AMENDS]->(cs:CodeSection)<-[:AMENDS]-()<-[:ADOPTED]-(c2:City) "
            "WHERE c1.name < c2.name "
            "WITH cs.ref AS section, collect(DISTINCT c1.display_name) + collect(DISTINCT c2.display_name) AS cities "
            "WHERE size(cities) >= 2 "
            "RETURN section, cities "
            "ORDER BY size(cities) DESC "
            "LIMIT 20"
        )
        shared_data = [dict(r) for r in shared]
        if shared_data:
            shared_text = "SECTIONS AMENDED BY MULTIPLE CITIES:\n"
            for s in shared_data:
                shared_text += f"  Section {s['section']}: amended by {', '.join(s['cities'])}\n"
            graph_context.append(shared_text)

        # 4. Get model code comparison (IBC vs CBC)
        model_comparison = session.run(
            "MATCH (c:City)-[:IN_STATE]->(s:State) "
            "RETURN c.display_name AS city, s.name AS state, c.model_code_family AS base_code, "
            "       c.population AS population "
            "ORDER BY s.name, c.display_name"
        )
        mc_data = [dict(r) for r in model_comparison]
        if mc_data:
            mc_text = "CITY PROFILES:\n"
            for m in mc_data:
                mc_text += f"  {m['city']} ({m['state']}): base code = {m['base_code']}, pop = {m['population']:,}\n"
            graph_context.append(mc_text)

    driver.close()
    return graph_context


def hybrid_answer(query: str, top_k: int = 10) -> EvalResult:
    """Hybrid pipeline: Neo4j graph context + vector retrieval + GPT-4o."""
    t0 = time.perf_counter()

    # Step 1: Get structured context from knowledge graph
    try:
        graph_context = graph_retrieve(query)
        graph_text = "\n\n".join(graph_context)
    except Exception as e:
        graph_text = f"[Graph retrieval error: {e}]"

    # Step 2: Get vector retrieval (same as baseline but more chunks)
    retrieval = baseline_retrieve(query, top_k)
    chunks = retrieval["chunks"]

    # Group by source for structured presentation
    by_source = {}
    for c in chunks:
        meta = c.get("metadata", {})
        key = f"{meta.get('city', 'unknown')} - {meta.get('original_filename', 'unknown')}"
        if key not in by_source:
            by_source[key] = []
        by_source[key].append(c)

    vector_parts = []
    for source_key, source_chunks in by_source.items():
        section_text = "\n".join(c.get("content", "") for c in source_chunks)
        vector_parts.append(f"=== Source: {source_key} ===\n{section_text}")
    vector_text = "\n\n".join(vector_parts)

    # Step 3: Combine graph + vector context and generate answer
    llm = ChatOpenAI(model="gpt-4o", temperature=0, api_key=OPENAI_KEY)
    response = llm.invoke(
        f"You are an expert building code analyst with access to a knowledge graph of "
        f"building code amendments across 8 US cities.\n\n"
        f"STRUCTURED KNOWLEDGE GRAPH DATA:\n{graph_text}\n\n"
        f"RETRIEVED DOCUMENT PASSAGES ({len(chunks)} passages from {len(by_source)} sources):\n{vector_text}\n\n"
        f"Instructions:\n"
        f"1. Use BOTH the knowledge graph data AND the retrieved passages to answer\n"
        f"2. Cite specific section numbers, table numbers, and ordinance numbers\n"
        f"3. When comparing across jurisdictions, use the graph data for context\n"
        f"4. If the information is not available, say so clearly\n"
        f"5. Structure your answer with the most relevant information first\n\n"
        f"Question: {query}"
    )

    total_time = (time.perf_counter() - t0) * 1000

    return EvalResult(
        query_id="",
        mode="hybrid",
        question=query,
        answer=response.content,
        retrieved_chunks=[
            {"content": c.get("content", "")[:200], "metadata": c.get("metadata", {}), "similarity": c.get("similarity", 0)}
            for c in chunks
        ],
        latency_ms=total_time,
    )


# ─── Scoring (LLM-as-Judge: GPT-4.1) ────────────────────────────────────────

# Judge model: GPT-4.1 (upgraded from GPT-4o to avoid self-preference bias,
# since GPT-4o generates the answers). GPT-4.1 has better instruction following
# and more reliable rubric-based scoring.

JUDGE_MODEL = "gpt-4.1"


def _parse_judge_response(content: str) -> dict:
    """Extract JSON scores from a judge response."""
    if "```" in content:
        match = re.search(r"```(?:json)?\s*(\{.*?\})\s*```", content, re.DOTALL)
        if match:
            content = match.group(1)
    start = content.find("{")
    end = content.rfind("}") + 1
    if start >= 0 and end > start:
        return json.loads(content[start:end])
    raise ValueError("No JSON object found in judge response")


def score_result(result: EvalResult, benchmark: BenchmarkQuery) -> EvalResult:
    """Score using GPT-4.1 as judge + heuristic metrics."""

    # 1. Fact Accuracy (with refusal detection)
    answer_lower = result.answer.lower()
    refusal_phrases = [
        "sorry", "not found", "don't have", "no information",
        "cannot find", "not available", "i'm unable", "insufficient",
        "does not include", "does not contain", "doesn't include",
        "doesn't contain", "only includes information about",
        "only contains information", "not in the provided context",
        "not available in the provided", "no relevant information",
        "does not have information", "outside the scope",
        "not covered in", "no data available",
    ]
    is_refusal = any(phrase in answer_lower for phrase in refusal_phrases)

    if is_refusal:
        result.fact_accuracy = 0.0 if benchmark.answerable else 1.0
    elif benchmark.ground_truth_facts:
        found = sum(
            1 for fact in benchmark.ground_truth_facts
            if fact.lower() in answer_lower
        )
        result.fact_accuracy = found / len(benchmark.ground_truth_facts)
    else:
        result.fact_accuracy = 1.0 if not benchmark.answerable else 0.0

    # 2. Completeness
    result.completeness = result.fact_accuracy

    # 3. Citation Recall
    if benchmark.required_citations:
        found_citations = sum(
            1 for cite in benchmark.required_citations
            if cite.lower() in result.answer.lower()
        )
        result.citation_recall = found_citations / len(benchmark.required_citations)
    else:
        result.citation_recall = 1.0

    # 4. LLM-as-Judge (GPT-4.1): Faithfulness + Relevance + Hallucination
    if is_refusal:
        result.faithfulness = 1.0 if not benchmark.answerable else 0.0
        result.relevance_score = 0.5
        result.hallucination_score = 0.0
        return result

    context_text = "\n".join(c["content"] for c in result.retrieved_chunks)

    judge_prompt = (
        "You are evaluating an AI assistant's answer about building codes.\n\n"
        f"Question: {benchmark.question}\n"
        f"Expected Answer: {benchmark.expected_answer_short}\n"
        f"AI Answer: {result.answer}\n"
        f"Retrieved Context (first 1000 chars): {context_text[:1000]}\n\n"
        "Score the answer on these dimensions (0.0 to 1.0):\n"
        "1. faithfulness: Is every claim in the answer supported by the retrieved context? "
        "Quote specific context passages to verify each claim.\n"
        "2. relevance: Does the answer address the question asked?\n"
        "3. hallucination: Does the answer contain fabricated information not in the context? "
        "(0=no hallucination, 1=heavy hallucination). "
        "Check each factual claim against the context.\n\n"
        'Respond in JSON format:\n'
        '{"faithfulness": 0.X, "relevance": 0.X, "hallucination": 0.X, "reasoning": "..."}'
    )

    try:
        judge_llm = ChatOpenAI(model=JUDGE_MODEL, temperature=0, api_key=OPENAI_KEY)
        response = judge_llm.invoke(judge_prompt)
        scores = _parse_judge_response(response.content)
        result.faithfulness = float(scores.get("faithfulness", 0.0))
        result.relevance_score = float(scores.get("relevance", 0.0))
        result.hallucination_score = float(scores.get("hallucination", 0.0))
    except Exception as e:
        print(f"    Judge error ({JUDGE_MODEL}): {e}")
        result.faithfulness = 0.0
        result.relevance_score = 0.0
        result.hallucination_score = 0.5

    return result


# ─── Statistical Testing ─────────────────────────────────────────────────────


def compare_results(baseline_results: list, hybrid_results: list) -> dict:
    """Statistical comparison of two result sets."""
    from scipy.stats import wilcoxon

    metrics = ["fact_accuracy", "completeness", "faithfulness", "citation_recall", "relevance_score"]
    comparison = {}

    for metric in metrics:
        b_scores = [getattr(r, metric) for r in baseline_results]
        h_scores = [getattr(r, metric) for r in hybrid_results]

        b_mean = np.mean(b_scores)
        h_mean = np.mean(h_scores)
        delta = h_mean - b_mean

        # Wilcoxon signed-rank test (paired, non-parametric)
        try:
            stat, p_value = wilcoxon(h_scores, b_scores, alternative="greater")
        except Exception:
            stat, p_value = 0, 1.0

        # Effect size (Cliff's delta)
        n = len(b_scores)
        more = sum(1 for h, b in zip(h_scores, b_scores) if h > b)
        less = sum(1 for h, b in zip(h_scores, b_scores) if h < b)
        cliffs_d = (more - less) / n if n > 0 else 0

        comparison[metric] = {
            "baseline_mean": round(b_mean, 4),
            "hybrid_mean": round(h_mean, 4),
            "delta": round(delta, 4),
            "wilcoxon_p": round(p_value, 6),
            "cliffs_delta": round(cliffs_d, 4),
            "significant": p_value < 0.05,
        }

    # Latency comparison
    b_latency = [r.latency_ms for r in baseline_results]
    h_latency = [r.latency_ms for r in hybrid_results]
    comparison["latency_ms"] = {
        "baseline_mean": round(np.mean(b_latency), 1),
        "hybrid_mean": round(np.mean(h_latency), 1),
        "baseline_p95": round(np.percentile(b_latency, 95), 1),
        "hybrid_p95": round(np.percentile(h_latency, 95), 1),
    }

    return comparison


# ─── Report Generation ────────────────────────────────────────────────────────


def generate_report(baseline_results: list, hybrid_results: list, comparison: dict) -> str:
    """Generate a markdown evaluation report."""
    lines = [
        "# RAG Evaluation Report: Hybrid vs. Baseline",
        "",
        f"**Date:** {time.strftime('%Y-%m-%d %H:%M')}",
        f"**Queries evaluated:** {len(baseline_results)}",
        f"**Baseline:** Vector-only (Supabase pgvector + GPT-4o)",
        f"**Hybrid:** Knowledge Graph (Neo4j) + Vector Search + GPT-4o",
        "",
        "## Summary",
        "",
        "| Metric | Baseline | Hybrid | Delta | p-value | Significant? |",
        "|--------|----------|--------|-------|---------|--------------|",
    ]

    for metric, data in comparison.items():
        if metric == "latency_ms":
            continue
        sig = "Yes **" if data["significant"] else "No"
        lines.append(
            f"| {metric} | {data['baseline_mean']:.3f} | {data['hybrid_mean']:.3f} | "
            f"{data['delta']:+.3f} | {data['wilcoxon_p']:.4f} | {sig} |"
        )

    if "latency_ms" in comparison:
        lat = comparison["latency_ms"]
        lines.extend([
            "",
            "## Latency",
            f"- Baseline mean: {lat['baseline_mean']:.0f}ms (p95: {lat['baseline_p95']:.0f}ms)",
            f"- Hybrid mean: {lat['hybrid_mean']:.0f}ms (p95: {lat['hybrid_p95']:.0f}ms)",
        ])

    # Per-category breakdown
    categories = {}
    for r in baseline_results:
        cat = next((q.category for q in BENCHMARK_QUESTIONS if q.id == r.query_id), "unknown")
        if cat not in categories:
            categories[cat] = {"baseline": [], "hybrid": []}
        categories[cat]["baseline"].append(r.fact_accuracy)

    for r in hybrid_results:
        cat = next((q.category for q in BENCHMARK_QUESTIONS if q.id == r.query_id), "unknown")
        if cat in categories:
            categories[cat]["hybrid"].append(r.fact_accuracy)

    lines.extend(["", "## Per-Category Fact Accuracy", ""])
    lines.append("| Category | Baseline | Hybrid | Delta |")
    lines.append("|----------|----------|--------|-------|")
    for cat, scores in categories.items():
        b = np.mean(scores["baseline"]) if scores["baseline"] else 0
        h = np.mean(scores["hybrid"]) if scores["hybrid"] else 0
        lines.append(f"| {cat} | {b:.3f} | {h:.3f} | {h - b:+.3f} |")

    # Individual results
    lines.extend(["", "## Individual Query Results", ""])
    for b, h in zip(baseline_results, hybrid_results):
        lines.extend([
            f"### {b.query_id}: {b.question[:80]}...",
            f"- **Baseline** accuracy={b.fact_accuracy:.2f} faithfulness={b.faithfulness:.2f} citations={b.citation_recall:.2f} ({b.latency_ms:.0f}ms)",
            f"- **Hybrid** accuracy={h.fact_accuracy:.2f} faithfulness={h.faithfulness:.2f} citations={h.citation_recall:.2f} ({h.latency_ms:.0f}ms)",
            "",
        ])

    return "\n".join(lines)


# ─── Main Runner ──────────────────────────────────────────────────────────────


def run_evaluation(mode: str = "baseline", queries: list = None):
    """Run evaluation on benchmark queries."""
    if queries is None:
        queries = BENCHMARK_QUESTIONS

    print(f"\n{'='*60}")
    print(f"Running {mode} evaluation on {len(queries)} queries")
    print(f"{'='*60}")

    results = []
    for i, q in enumerate(queries):
        print(f"\n[{i+1}/{len(queries)}] {q.id}: {q.question[:60]}...")

        if mode == "baseline":
            result = baseline_answer(q.question, top_k=5)
        elif mode == "multi_agent":
            # Phase 3: Multi-agent pipeline with specialized agents
            from multi_agent_rag import multi_agent_answer
            t0 = time.perf_counter()
            answer, chunks = multi_agent_answer(q.question, return_chunks=True)
            total_time = (time.perf_counter() - t0) * 1000
            result = EvalResult(
                query_id="", mode="multi_agent", question=q.question,
                answer=answer,
                retrieved_chunks=[
                    {"content": c.get("content", "")[:200], "metadata": c.get("metadata", {}), "similarity": c.get("similarity", 0)}
                    for c in chunks
                ],
                latency_ms=total_time,
            )
        else:
            # Hybrid mode: enhanced retrieval (top-10, re-ranked, metadata-enriched prompt)
            result = hybrid_answer(q.question, top_k=10)

        result.query_id = q.id

        # Score the result
        result = score_result(result, q)
        results.append(result)

        print(f"  accuracy={result.fact_accuracy:.2f} faith={result.faithfulness:.2f} "
              f"citations={result.citation_recall:.2f} halluc={result.hallucination_score:.2f} "
              f"({result.latency_ms:.0f}ms)")

    # Save results
    run_id = f"{mode}_{time.strftime('%Y%m%d_%H%M%S')}"
    result_path = os.path.join(EVAL_DIR, f"{run_id}.json")
    with open(result_path, "w") as f:
        json.dump([asdict(r) for r in results], f, indent=2)
    print(f"\nResults saved: {result_path}")

    # Summary
    print(f"\n{'='*60}")
    print(f"EVALUATION SUMMARY ({mode})")
    print(f"  Fact Accuracy:    {np.mean([r.fact_accuracy for r in results]):.3f}")
    print(f"  Completeness:     {np.mean([r.completeness for r in results]):.3f}")
    print(f"  Faithfulness:     {np.mean([r.faithfulness for r in results]):.3f}")
    print(f"  Citation Recall:  {np.mean([r.citation_recall for r in results]):.3f}")
    print(f"  Hallucination:    {np.mean([r.hallucination_score for r in results]):.3f}")
    print(f"  Avg Latency:      {np.mean([r.latency_ms for r in results]):.0f}ms")

    return results


