def load_benchmark_from_ground_truth(path="ground_truth_qa.json"):
    """Load benchmark queries from verified ground truth file."""
    import os
    full_path = os.path.join(os.path.dirname(__file__), path)
    if not os.path.exists(full_path):
        print(f"Ground truth file not found: {full_path}")
        print("Run: python generate_ground_truth.py first")
        return FALLBACK_BENCHMARK

    with open(full_path) as f:
        qa_pairs = json.load(f)

    queries = []
    for i, qa in enumerate(qa_pairs):
        city = qa.get("city", "unknown").lower().replace(" ", "_")
        queries.append(BenchmarkQuery(
            id=f"q{i+1:03d}",
            question=qa["question"],
            category="factual_lookup",  # Default; can be manually categorized later
            jurisdiction_scope=[city],
            year_scope=[],
            expected_answer_short=qa["answer"][:200],
            ground_truth_facts=qa.get("facts", []),
            required_citations=qa.get("citations", []),
        ))
    return queries


